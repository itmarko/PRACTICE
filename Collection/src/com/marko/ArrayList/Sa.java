package com.marko.ArrayList;

public class Sa {

	private int x;
	private String s;
	
	public Sa(int x, String s) {
		this.x = x;
		this.s = s;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	@Override
	public String toString() {
		return "Sa (x=" + x + ", s=" + s + ")";
	}
	
	
	
	
	
	
	
}
